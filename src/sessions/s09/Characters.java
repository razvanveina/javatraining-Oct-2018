package sessions.s09;

public class Characters {
	public static void main(String[] args) {
		System.out.println(Character.isDigit('4'));
		System.out.println(Character.isDigit('o'));
		System.out.println('\\');
		System.out.println('\"');

		System.out.println("a" + System.lineSeparator() + "b");
	}
}
